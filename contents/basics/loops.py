# While Loop
print("\n \n While Loop Example")
a = 2
while a < 9 :
    print(a)
    a=a+1

# For Loop

print("\n \n For Loop Example")
list = ['a','b','c','d']
for value in list :
    print(value)

# "break" and "continue" statements

# Prints out 0,1,2,3,4

count = 0
while True:
    print(count)
    count += 1
    if count >= 5:
        break

# Prints out only odd numbers - 1,3,5,7,9
for x in range(10):
    # Check if x is even
    if x % 2 == 0:
        continue
    print(x)

# "else" clause for loops
# Prints out 0,1,2,3,4 and then it prints "count value reached 5"

count=0
while(count<5):
    print(count)
    count +=1
else:
    print("count value reached %d" %(count))

